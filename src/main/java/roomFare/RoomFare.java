package roomFare;

public class RoomFare {
    private int id;
    private double value;
    private String season;

    public RoomFare(int id, double value, String season) {
        this.id = id;
        this.value = value;
        this.season = season;
    }

    public int getId() {
        return id;
    }


    public double getValue() {
        return value;
    }


    public String getSeason() {
        return season;
    }
}










